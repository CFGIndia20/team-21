package com.example.umeed;

public class WorkData {

    private String desc;
    private int empId;

    public WorkData(String desc, int empId) {
        this.desc = desc;
        this.empId = empId;
    }

    public String getDesc() {
        return desc;
    }

    public int getEmpId() {
        return empId;
    }
}
